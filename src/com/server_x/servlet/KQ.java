package com.server_x.servlet;

public class KQ {
	private String ten;
    private String kq;
    
	public KQ(String ten, String kq) {
		super();
		this.ten = ten;
		this.kq = kq;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public String getKq() {
		return kq;
	}
	public void setKq(String kq) {
		this.kq = kq;
	}
    
}
